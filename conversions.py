def mm_to_cm(mm):
    return mm / 10

def cm_to_mm(cm):
    return cm * 10

def cm_to_m(cm):
    return cm / 100

def m_to_cm(m):
    return m * 100

def inch_to_mm(inch):
    return inch * 25.4

def mm_to_inch(mm):
    return mm / 25.4
def g_to_kg(value):
    return value / 1000

def kg_to_g(value):
    return value * 1000

def c_to_f(value):
    return (value * 9/5) + 32

def f_to_c(value):
    return (value - 32) * 5/9
